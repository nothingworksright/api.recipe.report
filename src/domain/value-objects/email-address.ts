export class EmailAddress {
  private _value: string

  public constructor(value: string) {
    this._value = value
  }

  public get email_address(): string {
    return this._value
  }

  public valid(value: string): boolean {
    if (!value) return false
    const regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
    return regex.test(value)
  }
}
